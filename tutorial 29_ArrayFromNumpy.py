from numpy import *

arr_1 = array([1,2,3,4,5])
print(arr_1)

arr_2 = array([1,2,3,4,5], float)
print(arr_2)

arr_lin = linspace(0,20,6)
print(arr_lin)


arr_arange = arange(0,20,6)
print(arr_arange)

arr_log = logspace(0,20,6)
print(arr_log)

arr_zeros = zeros(5)
print(arr_zeros)


arr_ones = ones(5)
print(arr_ones)